    private void suboxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_suboxMouseClicked

        String regex1 = "^[a-zA-Z0-9\\_]{7,15}$";
        user = userbox.getText();

        String regex2 = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        email = mailbox.getText();

        String regex3 = "^[A-Z]+[0-9]{4,8}$";
        pass = String.valueOf(passbox.getPassword());

        if (userbox.getText().matches(regex1) && mailbox.getText().matches(regex2) && passbox.getText().matches(regex3)) {
            JOptionPane.showMessageDialog(this, "Sign up successful", "Success", JOptionPane.INFORMATION_MESSAGE);
            userbox.setText("");
            ubox.setVisible(true);
            mailbox.setText("");
            mbox.setVisible(true);
            passbox.setText("");
            pbox.setVisible(true);
        } else if (userbox.getText().matches(regex1) && !mailbox.getText().matches(regex2) && passbox.getText().matches(regex3)) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!userbox.getText().matches(regex1) && mailbox.getText().matches(regex2) && passbox.getText().matches(regex3)) {
            JOptionPane.showMessageDialog(this, "Invalid Username", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (userbox.getText().matches(regex1) && mailbox.getText().matches(regex2) && !passbox.getText().matches(regex3)) {
            JOptionPane.showMessageDialog(this, "Invalid Password", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Sign Up not Successful \n" + "Input a Valid Data! \n" + "Read Error Details!", "Notice", JOptionPane.WARNING_MESSAGE);
        }

        data[0] = user;
        data[1] = email;
        data[2] = pass; // this basically decides whether to accept values from user and store in the array.
        // it is based on the label1 message// TODO add your handling code here:

        for (int i = 0; i < data.length; i++) {
            System.out.println(data[i]);
        }


    }//GEN-LAST:event_suboxMouseClicked

